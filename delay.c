#include "STD_TYPES.h"
#include "BIT_MATH.h"

void delay_s (u32 Copy_u32Time)
{
	u32 itr;
	Copy_u32Time *=100000;
	for(itr=0;itr<Copy_u32Time;itr++);
}
void delay_ms (u32 Copy_u32Time)
{
	u32 itr;
	Copy_u32Time *=100;
	for(itr=0;itr<Copy_u32Time;itr++);
}
void delay_us (u32 Copy_u32Time)
{
	u32 itr;
	for(itr=0;itr<Copy_u32Time;itr++);
}
